#!/usr/bin/env python3
"""
802.11ax Packet Type Statistics and Airtime Analysis Script

This script automatically parses the PCAPs generated by the IEEE 802.11ax simulation
examples, calculates frame counts, percentages, size distributions (mean and standard
deviation in bytes), and estimated physical airtime occupancy percentages for each frame
type, and integrates the formatted markdown tables directly into the walkthrough.md of
each example directory.

Usage:
    python3 examples/ieee80211ax/analysis/analyze_pcap_types.py

Requirements:
    - Python 3.x
    - tshark (Wireshark command-line utility) in system PATH
    - Runs from the project root directory
"""

